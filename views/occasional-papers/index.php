            <p>
                Published on behalf of the Center for Research & Graduate Education (CRGE), DKU Occasional Papers are one outlet of the Kazakh-German University (DKU) academic community. Launched in winter 2020, the series appears irregularly several times per year. Authors are DKU faculty or select graduate students, guest lecturers from Germany, DKU Visiting Fellows and academic partners with whom the DKU collaborates. This original series covers the wide field of themes offered at the DKU, with geographic attention being paid to Kazakhstan, other Central Asian countries and their relationships with Germany and the European Union. Themes include regional cooperation and security, the roles of external actors in Central Asia, the history and culture of ethnic Germans in Kazakhstan, water, climate change, sustainable development, renewable resources, and logistics and digitization. While principally targeted at scientific communities and practitioners, studies appearing in the series are written to be accessible to the non-academic community as well.
            </p>

            <p>
                Two types of Occasional Papers are featured. Occasional Papers (Research) are academic pieces that cover a given theme thoroughly with conceptual rigor, either empirically or explicitly theoretically. In contrast, Occasional Papers (Policy) are shorter studies which analyze a specific question and main argument, though with immediate real-world relevance and findings that are instantly applicable to practitioners. These latter papers are meant to launch or contribute to debates, not the least those regarding Kazakh-German relations. Usually peer reviewed blindly by at least one reviewer, the DKU Occasional Papers are exclusively published in the English language. Below you will soon find our first Occasional Paper. We will later set up an <a><span>archive</span></a> for previously published and distributed papers.
            </p>
            <p>
                The CRGE welcomes enquiries and suggestions for contributions to our series at <a title="crge-info@dku.kz" href="mailto:crge-info@dku.kz">crge-info@dku.kz</a>.
            </p>
